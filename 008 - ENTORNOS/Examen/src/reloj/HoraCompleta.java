package reloj;

public class HoraCompleta {
	
	public HoraCompleta(int hora, int minutos) {
		this.hora = hora;
		this.minutos = minutos;
	}

	private int hora;
	private int minutos;
	
	public HoraCompleta() {
		
	}

	public int getHora() {
		return hora;
	}

	public void setHora(int hora) {
		this.hora = hora;
	}

	public int getMinutos() {
		return minutos;
	}

	public void setMinutos(int minutos) {
		this.minutos = minutos;
	}
	
	
	
	

}
